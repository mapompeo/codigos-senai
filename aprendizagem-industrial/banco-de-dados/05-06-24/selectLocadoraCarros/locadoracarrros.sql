CREATE SCHEMA LOCADORACARROS;
USE LOCADORACARROS;

CREATE TABLE CLIENTES (
	CPF NUMERIC(11) NOT NULL PRIMARY KEY,
    NOME VARCHAR(255) NOT NULL,
    FONE NUMERIC(11) NOT NULL
);

CREATE TABLE CARROS (
	COD VARCHAR(4) NOT NULL PRIMARY KEY,
    MODELO VARCHAR(255) NOT NULL,
    VALOR NUMERIC(4) NOT NULL
);

CREATE TABLE COMPRA (
	COD NUMERIC(3) NOT NULL PRIMARY KEY,
    FK_CPF NUMERIC(11) NOT NULL,
    FK_COD VARCHAR(4) NOT NULL,
    CONSTRAINT FK_CPF FOREIGN KEY (FK_CPF) REFERENCES CLIENTES (CPF),
	CONSTRAINT FK_COD FOREIGN KEY (FK_COD) REFERENCES CARROS (COD)
);

