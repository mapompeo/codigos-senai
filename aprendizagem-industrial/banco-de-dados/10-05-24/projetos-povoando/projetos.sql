-- CRIANDO AS TABELAS

CREATE TABLE CARGOS (
COD_CARGO NUMERIC(1) NOT NULL PRIMARY KEY,
    NM_CARGO VARCHAR(100) NOT NULL
);

CREATE TABLE FUNCIONARIO (
MATRICULA NUMERIC(3) NOT NULL PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL,
    COD_CARGOS_F NUMERIC(1) NOT NULL,
    CONSTRAINT COD_CARGOS_F FOREIGN KEY (COD_CARGOS_F) REFERENCES CARGOS (COD_CARGO)
);

CREATE TABLE PROJETO (
COD_PROJ NUMERIC(2) NOT NULL PRIMARY KEY,
    NM_PROJ VARCHAR(100) NOT NULL,
    DT_FIM DATE NOT NULL
);

CREATE TABLE FUNCIONARIO_PROJETO (
HORAS NUMERIC(4) NOT NULL,
    MATRICULA_FP NUMERIC(3) NOT NULL,
COD_PROJ_FP NUMERIC(2) NOT NULL,
CONSTRAINT MATRICULA_FP FOREIGN KEY (MATRICULA_FP) REFERENCES FUNCIONARIO (MATRICULA),
    CONSTRAINT COD_PROJ_FP FOREIGN KEY (COD_PROJ_FP) REFERENCES PROJETO (COD_PROJ)
);

CREATE TABLE CLIENTE (
COD_CLI NUMERIC(4) NOT NULL PRIMARY KEY,
    NM_CLIENTE VARCHAR(100) NOT NULL
);

CREATE TABLE CONTATO (
TELEFONE NUMERIC(11) NOT NULL PRIMARY KEY,
    COD_CLI_C NUMERIC(4) NOT NULL,
    CONSTRAINT COD_CLI_C FOREIGN KEY (COD_CLI_C) REFERENCES CLIENTE (COD_CLI)
);


